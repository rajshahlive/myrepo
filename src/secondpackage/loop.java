public class loop {

	
	public static void main(String[] args) {
		int i = 0;
	     
	     while(i < 5)
	     {
	       System.out.println("i is : " + i);
	       i++;
	     }
	     
	     for(i=0; i < 5 ; i++)
	         System.out.println("Int is : " + i);
	     
	     for(i=0; i < 5 ; i++)
	      {
	      System.out.println("I is : " + i);
	      i++;
	      }

	}

}
